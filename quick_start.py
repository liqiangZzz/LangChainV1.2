"""
Agent 快速开始示例。

本文件用一个天气查询工具演示最基础的 create_agent + invoke 流程。
运行会调用真实 DeepSeek 模型，执行前需要确认 .env 中配置了 DeepSeek 信息。
"""
from langchain.tools import tool
from langchain.agents import create_agent

from models.init_chat_model.init_chat_model_llm import deepseek_llm


# =====================================================================
# 1. 定义工具 —— 给 Agent 一把查询天气的小锤子
# =====================================================================
@tool
def get_weather(city: str):
    """获取给定城市的天气。

    Args:
        city: 城市名称。
    """
    # 模拟天气查询：这里不接真实天气 API，只返回固定结果。
    return f"{city} 天气晴朗！"


# =====================================================================
# 2. 创建 Agent —— 绑定模型、工具和系统提示词
# =====================================================================
agent = create_agent(
    model=deepseek_llm,
    tools=[get_weather],
    system_prompt='你是一个助手，你可以查询城市的天气。'
)


# =====================================================================
# 3. 调用 Agent —— 让模型根据用户问题决定是否使用工具
# =====================================================================
resp = agent.invoke(
    {"messages": [{"role": "user", "content": "查询上海的天气"}]}
)


# =====================================================================
# 4. 打印完整结果 —— 快速观察 messages 和工具调用过程
# =====================================================================
print(resp)
